public class ElseIFDemo 
{

	public static void main(String[] args) 
	{
		int a=20,b=10;
		
		if(a>b)
		{
			System.out.println("a is greater");
		}
		else if(b>a)
		{
			System.out.println("b is greater");
		}
		else
		{
			System.out.println("Both A & B are equal..");
		}

	}

}
